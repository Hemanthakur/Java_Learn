package Inheritence;

public class Person {
protected String name;
   public Person(String name) {
	   this.name=name;
   }
	public void walk() {
		System.out.println(name+" is walking");
	}
	public void eat() {
		System.out.println(name+" is eating");
	}
	public void sleep() {
		System.out.println(name+" is sleeping");
	}
	public void laugh() {
		System.out.println(name+" is laughinghgggggg");
	}
}
